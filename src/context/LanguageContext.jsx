const LanguageContext = createContext(null);

const getValue = (obj, path) =>
  path.split(".").reduce((acc, key) => (acc && acc[key] !== undefined ? acc[key] : undefined), obj);

const interpolate = (value, params) => {
  if (typeof value !== "string" || !params) {
    return value;
  }

  return Object.entries(params).reduce(
    (result, [key, replacement]) => result.replaceAll(`{{${key}}}`, replacement),
    value
  );
};

export const LanguageProvider = ({ children }) => {
  const [language, setLanguage] = useState(() => {
    if (typeof window === "undefined") {
      return "en";
    }

    return window.localStorage.getItem(STORAGE_KEY) || "en";
  });

  useEffect(() => {
    if (typeof window !== "undefined") {
      window.localStorage.setItem(STORAGE_KEY, language);
      document.documentElement.lang = language === "hi" ? "hi" : "en";
    }
  }, [language]);

  const value = useMemo(() => {
    const t = (key, fallback = key, params) =>
      interpolate(getValue(translations[language], key) ?? getValue(translations.en, key) ?? fallback, params);
    const translateCategory = (category) => t(`categories.${category}`, category);

    return {
      language,
      setLanguage,
      toggleLanguage: () => setLanguage((current) => (current === "en" ? "hi" : "en")),
      t,
      translateCategory,
    };
  }, [language]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);

  if (!context) {
    throw new Error("useLanguage must be used within LanguageProvider");
  }

  return context;
};
